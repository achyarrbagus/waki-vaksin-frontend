export function switchName(param) {
  let value;

  switch (param) {
    case "en":
      value = "Cepat Sehat";
      break;
    case "id":
      value = "Cepat Sehat";
      break;
    case "ph":
      value = "Cepat Sehat";
      break;
    case "vn":
      value = "Cepat Sehat";
      break;
    case "th":
      value = "Cepat Sehat";
      break;
    case "kh":
      value = "Cepat Sehat";
      break;
    case "pk":
      value = "Cepat Sehat";
      break;
    default:
      value = "Cepat Sehat";
      break;
  }

  return value;
}
