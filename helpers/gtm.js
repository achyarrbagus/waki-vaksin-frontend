export function switchGTM(param) {
  switch (param) {
    case "en":
      return "";
    case "id":
      return "GTM-TQL3MSS";
    case "ph":
      return "GTM-KVNGDWV";
    case "vn":
      return "";
    case "th":
      return "";
    case "kh":
      return "";
    case "pk":
      return "";
    default:
      return "";
  }
}
